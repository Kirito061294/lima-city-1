                <h1>Corona-Impfstoffe in Deutschland aus ethischer Sicht</h1><br />
				<h2>Der Verein „Ärzte für das Leben e.V.“ ist nicht gegen eine Corona-Impfung, <br />
				die einen wichtigen Schritt zur Eindämmung der Pandemie darstellen kann.<br /><br />
				Wir sehen es jedoch als unsere Aufgabe an, <br />
				auf die ethische Problematik der in Deutschland zugelassenen bzw. kurz vor der Zulassung stehenden Impfstoffe hinzuweisen.</h2><br /><br />
				<h3>
				<font color="black">
				    Zwei verschiedene Impfstoffarten sind derzeit zur Corona-Impfung in Deutschland zu nennen:<br />
					<br />
					1. Vektorimpfstoffe: <br />
					Derzeitige Vektorimpfstoffe sind die der Firmen: AstraZeneca, Janssen (Johnson&Johnson) und der russische Sputnik V-Impfstoff.<br />
					Alle Vektorimpfstoffe werden nach unserer Information mit Hilfe von Zelllinien aus abgetriebenen Kindern produziert und getestet. Sie sind damit als unethisch zu bezeichnen.<br />
					<br />
					2. mRNA-Impfstoffe: <br />
					Diese sind die Impfstoffe der Firmen: BioNTech/Pfizer, der Firma Moderna sowie der in Bälde zugelassene Impfstoff der Firma Curevac.<br />
					Beim mRNA-Impfstoff der Firma BioNTech/Pfizer werden zur Produktion des Impfstoffs keine Zelllinien aus abgetriebenen Kindern verwendet, wohl aber zu dessen Testung.<br />
					Beim mRNA-Impfstoff der Firma Moderna ist der ethische Aspekt schwieriger zu beurteilen. Bei der Forschung (mehrere Patente), Entwicklung und Testung des Impfstoffs kamen Zelllinien aus abgetriebenen Kindern zur Anwendung. Die ethische Situation bezüglich der Produktion der mRNA für das Spike-Proteine (d.h. der eigentliche virale Genträger des Impfstoffs), die in einer anderen, Moderna angeschlossenen Firma hergestellt wird, ist auch unklar, da in der wissenschaftlichen Literatur Arbeiten existieren, die eine Verwendung von Zelllinien aus abgetriebenen Kindern nahelegen.<br />
					Zum heutigen Zeitpunkt scheint der kurz vor der Zulassung stehende mRNA-Impfstoff der Firma Curevac aus Tübingen der einzige Corona-Impfstoff in Deutschland zu sein, bei dem keinerlei Verwendung von Zelllinien aus abgetriebenen Kindern erwähnt wird.<br />
					Andere ethische Corona-Impfstoffe befinden sich in der Entwicklung.<br />
					<br />
					Im Gegensatz zur Masern-Pflichtimpfung in Deutschland ist die Corona-Impfung freiwillig. Der Einzelne muss also für sich entscheiden, ob er sie bereits jetzt benötigt, oder ob er warten kann, bis ethisch unbedenkliche Impfstoffe verfügbar sind. Dabei muss ihm klar sein, dass er ohne Impfung sich und andere im besonderen Maße gegen die Infektion schützen muss, und unter Umständen eine Covid-19 Erkrankung riskiert.<br />
                    <br />
					Wir sind verpflichtet, diese Fakten gemäß unserer Möglichkeiten zu verbreiten, damit sich in Zukunft an der Impfstoffherstellung etwas ändert. Deshalb würden wir begrüßen, wenn ethisch unbedenkliche Impfstoffe eine Kennzeichnung erhielten, dass keine Zelllinien von abgetriebenen Kindern – in welcher Form und an welcher Stelle auch immer – bei Ihrer Entwicklung und Produktion zum Einsatz gekommen sind.<br />
